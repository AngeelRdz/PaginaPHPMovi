Uso de Web beacons.- También conocidos como etiquetas de Internet, etiquetas de píxel y clear GIFs. Movistar puede utilizar en su sitios web y en sus correos electrónicos con formato HTML los Web beacons, solos o en combinación con las cookies, para recopilar información sobre el uso de los sitios web y su interacción con el correo electrónico. El Web beacon es una imagen electrónica, llamada de un solo píxel (1x1) o GIF que puede reconocer información que es procesada en su computadora, como el caso de las cookies, la hora y fecha en que el sitio y sus secciones son visualizados.

Vínculos en los correos electrónicos de Movistar.- Los correos electrónicos pueden incluir vínculos que permiten a Movistar saber si usted activó dicho vínculo y visitó la página web de destino, pudiendo esta información ser incluida en su perfil. Asimismo, pueden incluir vínculos diseñados para dirigirlo a las secciones relevantes de los sitios Web, al re-direccionarlo a través de los servidores de Movistar el sistema de re-direccionamiento permite determinar la eficacia de las campañas de marketing en línea.

Protección a menores, a personas en estado de interdicción o incapacidad.- Movistar alienta a los padres y/o tutores a tomar un papel activo en las actividades en línea de sus hijos o representados. En caso de que considere que los datos personales han sido proporcionados por un menor o por una persona en estado de interdicción o incapacidad, en contravención al presente Aviso, por favor envíe un correo electrónico a la dirección: datos.personales.mx@telefonica.com para que Movistar proceda a eliminar tales datos personales a la brevedad.</p>
							  
