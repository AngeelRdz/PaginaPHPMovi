            //miro a ver si el estado está definido 
            if (estado != 0) { 
                //si estaba definido, entonces coloco las opciones de la ciudad correspondiente. 
                //selecciono el array de la ciudad adecuada 
                mis_ciudades=eval("ciudades_" + estado) 
                //calculo el numero de provincias 
                num_ciudades = mis_ciudades.length 
                //marco el número de provincias en el select 
                document.f1.ciudad.length = num_ciudades 
                //para cada ciudad del array, la introduzco en el select 
                for(i=0; i<num_ciudades; i++){ 
                    document.f1.ciudad.options[i].value=mis_ciudades[i] 
                    document.f1.ciudad.options[i].text=mis_ciudades[i] 
                }	
            }else{ 
                //si no había ciudad seleccionada, elimino las ciudades del select 
                document.f1.ciudad.length = 1 
